package Type

fun main(){
    val a: Byte = Byte.MAX_VALUE // 127
    println(a)
    val b: Byte = Byte.MIN_VALUE // - 128
    println(b)
    val c:Short=Short.MAX_VALUE // 32767
    println(c)
    val d:Short=Short.MIN_VALUE // 32768
    println(d)
    val e:Int= Int.MAX_VALUE // 2147483647
    println(e)
    val f:Int= Int.MIN_VALUE // -2147483648
    println(f)
    val g: Float = Float.MAX_VALUE
    println(g)
    val n: Float = Float.MIN_VALUE
    println(n)
    val m: Double = Double.MAX_VALUE
    println(m)
    val s: Double = Double.MIN_VALUE
    println(s)
}